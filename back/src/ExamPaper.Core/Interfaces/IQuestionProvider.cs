using System.Collections.Generic;

namespace ExamPaper.Core.Interfaces;

/// <summary>
///     Определяет контракт для компонента, обеспечивающего доступ к коллекции вопросов.
/// </summary>
/// <remarks>
///     Данный интерфейс отвечает только за чтение данных.
///     Он изолирует бизнес-логику от конкретной реализации хранилища (JSON, база данных или внешний API).
/// </remarks>
public interface IQuestionProvider
{
    /// <summary>
    ///     Извлекает все доступные вопросы из источника данных.
    /// </summary>
    /// <returns>
    ///     Перечисление объектов, реализующих <see cref="IQuestion" />.
    ///     Если вопросы отсутствуют, возвращает пустую коллекцию.
    /// </returns>
    /// <remarks>
    ///     Метод не должен изменять состояние хранилища.
    ///     Порядок возвращаемых вопросов не гарантируется, если он не определен реализацией.
    /// </remarks>
    IEnumerable<IQuestion> GetAllQuestions();
}